                                    <p>

                                        Audit Management System-DAC (AMS-DAC) adalah Sistem Informasi yang bertujuan untuk mengatur Audit

                                        Process Grup Internal Audit (GIA). pada Sistem ini juga bertugas dalam menjembatani antara Auditor

                                        selaku eksekutor dan Management sebagai pengawas auditor. AMS-DAC dikembangkan dalam web-based.



                                        AMS-DAC memiliki lima utama yang terkait dengan metode Proses Audit, yaitu; Back Office, Target &

                                        Resourcing, Eksekusi, Pelaporan dan Manajemen Pengetahuan. Setiap menu utama memiliki sub menu di

                                        dalamnya.

                                    </p>

